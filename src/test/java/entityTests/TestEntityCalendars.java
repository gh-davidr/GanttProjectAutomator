package entityTests;

import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.Test;
import org.richardson.david.entity.gantt.Calendars;

import jakarta.xml.bind.JAXBContext;
import jakarta.xml.bind.Unmarshaller;

public class TestEntityCalendars extends TestEntityBase {

	private static String sIMPLE_TASK_TEString = "<?xml version=\"1.0\" encoding=\"UTF-8\"?>"
			+ 	
			"   <calendars>\r\n"
			+ "        <day-types>\r\n"
			+ "            <day-type id=\"0\"/>\r\n"
			+ "            <day-type id=\"1\"/>\r\n"
			+ "            <default-week id=\"1\" name=\"default\" sun=\"1\" mon=\"0\" tue=\"0\" wed=\"0\" thu=\"0\" fri=\"0\" sat=\"1\"/>\r\n"
			+ "            <only-show-weekends value=\"false\"/>\r\n"
			+ "            <overriden-day-types/>\r\n"
			+ "            <days/>\r\n"
			+ "        </day-types>\r\n"
			+ "    </calendars>"
			;

	@Test
	void test1() {
		Calendars element = null;

		try {
			JAXBContext jaxbContext = JAXBContext.newInstance(Calendars.class);
			Unmarshaller unmarshaller = jaxbContext.createUnmarshaller();

			element = (Calendars)unmarshaller.unmarshal(stringToStream(sIMPLE_TASK_TEString));
		}
		catch (Exception e)
		{
			element = null;
			e.printStackTrace();
		}

		Assertions.assertTrue(element != null);
	}
	

}
